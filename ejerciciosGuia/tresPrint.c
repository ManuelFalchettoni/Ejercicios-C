#include <stdio.h>

int main() {
    // Declaramos las variables y asignamos los valores
    char nombre[] = "Juan Perez";
    char direccion[] = "Calle Falsa 123, Ciudad Ejemplo";
    char telefono[] = "+34 123 456 789";

    // Usamos printf para mostrar los datos desde las variables
    printf("Nombre: %s\n", nombre);
    printf("Direccion: %s\n", direccion);
    printf("Telefono: %s\n", telefono);

    return 0;
}


